package day10;

public class Calc {

	public static int sum(int x, int y) {
		return x + y;
	}

	public static int multiple(int x, int y) {
		return x * y;
	}

	public static int multiple(int x, int y, int z) {
		return x * y * z;
	}

	public static double multiple(double x, double y, double z) {
		return x * y * z;
	}

}
